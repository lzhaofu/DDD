
namespace DDD.Utility.Const
{
    /// <summary>
    /// 标点符号
    /// </summary>
    public static class PunctuationChar
    {
        /// <summary>
        /// 逗号
        /// </summary>
        public const char Comma = ',';
        /// <summary>
        /// 分号
        /// </summary>
        public const char Semicolon = ';';
        /// <summary>
        /// 句号
        /// </summary>
        public const char Period = '.';
        /// <summary>
        /// 竖线
        /// </summary>
        public const char VerticalLine = '|';
        /// <summary>
        /// 单引号
        /// </summary>
        public const char SingleQuotes  = '\'';
    }
}
